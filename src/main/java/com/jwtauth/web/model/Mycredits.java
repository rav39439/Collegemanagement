package com.jwtauth.web.model;

public class Mycredits {

	
	private String username;
	
	private int credits;
	private String semname;
	private int noofexam;
	private String feepay;
	
	
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public int getCredits() {
		return credits;
	}
	public void setCredits(int credits) {
		this.credits = credits;
	}
	public String getSemname() {
		return semname;
	}
	public void setSemname(String semname) {
		this.semname = semname;
	}
	public int getNoofexam() {
		return noofexam;
	}
	public void setNoofexam(int noofexam) {
		this.noofexam = noofexam;
	}
	public String getFeepay() {
		return feepay;
	}
	public void setFeepay(String feepay) {
		this.feepay = feepay;
	}
	
	
}
