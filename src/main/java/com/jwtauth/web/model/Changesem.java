package com.jwtauth.web.model;

public class Changesem {
	
	
	
	private String newsem;
	
	private String coursename;
	private String tname;
	
	private String oldsem;
	
	
	
	
	
	
	public String getTname() {
		return tname;
	}
	public void setTname(String tname) {
		this.tname = tname;
	}
	public String getOldsem() {
		return oldsem;
	}
	public void setOldsem(String oldsem) {
		this.oldsem = oldsem;
	}
	public String getNewsem() {
		return newsem;
	}
	public void setNewsem(String newsem) {
		this.newsem = newsem;
	}
	public String getCoursename() {
		return coursename;
	}
	public void setCoursename(String coursename) {
		this.coursename = coursename;
	}
	

}
